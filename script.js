function simulateLogin() {
    const firstName = document.getElementById('firstNameInput').value.trim();
    const lastName = document.getElementById('lastNameInput').value.trim();
    const email = document.getElementById('emailInput').value.trim();
    const password = document.getElementById('passwordInput').value.trim();
    const birthDate = document.getElementById('birthDateInput').value;

    // მკაცრი ანტი-ინტრიგანი ვალიდაცია
    if (!firstName || !lastName || !email || !password || !birthDate) {
        alert("გთხოვთ შეავსოთ ყველა ველი! / Please fill in all fields!");
        return;
    }

    if (!email.toLowerCase().endsWith('@gmail.com')) {
        alert("ელ-ფოსტა აუცილებლად უნდა მთავრდებოდეს @gmail.com-ით! / Email must end with @gmail.com!");
        return;
    }

    if (password.length < 6) {
        alert("პაროლი უნდა შედგებოდეს მინიმუმ 6 სიმბოლოსგან! / Password must be at least 6 characters!");
        return;
    }

    const birthYear = new Date(birthDate).getFullYear();
    if (birthYear > 2026) {
        alert("დაბადების წელი არ შეიძლება იყოს 2026-ზე გვიან! / Birth year cannot be later than 2026!");
        return;
    }

    currentUser = { firstName, lastName, email, password, birthDate };
    saveProfile(currentUser);
    renderNavStatus();
    closeModal('authModal');
}