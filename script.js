let hasBonusAccess = false;
const badWords = ["ლანძღვა", "გინება", "სუიციდი", "ბოროტი", "ცუდი ხარ"];

const STORAGE_KEYS = {
    profile: 'tole_user_profile',
    lang: 'tole_lang',
    bonus: 'tole_bonus_unlocked'
};

function escapeHtml(str) {
    if (!str) return '';
    const div = document.createElement('div');
    div.textContent = str;
    return div.innerHTML;
}

function t(key) {
    const dict = translations[currentLang] || translations.ka;
    return dict[key] !== undefined ? dict[key] : (translations.ka[key] || key);
}

function containsBadWord(text) {
    const lower = text.toLowerCase();
    return badWords.some(word => lower.includes(word));
}

function analyzeMethodText(text) {
    const cleaned = text.replace(/\s+/g, '');
    if (cleaned.length < 25) return { valid: false, reasonKey: 'upload_too_short' };
    return { valid: true };
}

function setLanguage(lang) {
    if (!translations[lang]) lang = 'ka';
    currentLang = lang;
    localStorage.setItem(STORAGE_KEYS.lang, lang);
    document.documentElement.lang = lang;
    document.getElementById('langSelect').value = lang;

    // ბრაუზერის სათაურის იძულებითი შენარჩუნება
    document.title = "Tole.ge - " + t('hero_title');

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.setAttribute('placeholder', t(key));
    });

    const toggle = document.getElementById('isPublicToggle');
    const label = document.getElementById('toggleLabel');
    if (toggle && label) {
        label.textContent = toggle.checked ? t('toggle_public') : t('toggle_private');
    }

    updateInstruction();
    renderNavStatus();
}

function loadStoredProfile() {
    try {
        const raw = localStorage.getItem(STORAGE_KEYS.profile);
        return raw ? JSON.parse(raw) : null;
    } catch (e) {
        return null;
    }
}

function saveProfile(profile) {
    localStorage.setItem(STORAGE_KEYS.profile, JSON.stringify(profile));
}

function renderNavStatus() {
    const nav = document.getElementById('userNavStatus');
    if (currentUser) {
        const badge = hasBonusAccess ? `<span class="bonus-badge">${escapeHtml(t('verified_badge'))}</span>` : '';
        nav.innerHTML = `
            <div class="user-chip">
                <span>👋 ${t('greeting')}, <strong>${escapeHtml(currentUser.firstName)}</strong></span>
                ${badge}
                <span class="logout-link" onclick="logout()">${escapeHtml(t('logout'))}</span>
            </div>
        `;
    } else {
        nav.innerHTML = `<button class="btn btn-secondary" data-i18n="nav_login" onclick="openModal('authModal')">${escapeHtml(t('nav_login'))}</button>`;
    }
}

function logout() {
    currentUser = null;
    localStorage.removeItem(STORAGE_KEYS.profile);
    renderNavStatus();
}

function unlockBonusCategory() {
    hasBonusAccess = true;
    localStorage.setItem(STORAGE_KEYS.bonus, 'true');

    Object.keys(instructionsDataByLang).forEach(lang => {
        if (!instructionsDataByLang[lang].bonus_stress) {
            instructionsDataByLang[lang].bonus_stress = {
                child: translations[lang] ? translations[lang].bonus_child : translations.ka.bonus_child,
                teen: translations[lang] ? translations[lang].bonus_teen : translations.ka.bonus_teen,
                adult: translations[lang] ? translations[lang].bonus_adult : translations.ka.bonus_adult
            };
        }
    });

    const select = document.getElementById('habitSelect');
    if (!select.querySelector('option[value="bonus_stress"]')) {
        const opt = document.createElement('option');
        opt.value = 'bonus_stress';
        opt.setAttribute('data-i18n', 'bonus_category_name');
        opt.textContent = t('bonus_category_name');
        select.appendChild(opt);
    }
    renderNavStatus();
}

